// ???????????????????????? File Modules ??????????????????????????
const viewRouter = require('./src/routes/viewRouter');
const userRouter = require('./src/routes/userRouter');
const morgan = require('./src/utils/morganCustom')

// ???????????????????????? Node Modules ??????????????????????????
const path = require('path');

// ??????????????????????? Vendor Modules ?????????????????????????
const chalk = require('chalk');
const cookieSession = require('cookie-session')
const express = require('express');
const methodOverride = require('method-override');

// ????????????????????????????????????????????????????????????????
// ???????????????????????? Application ???????????????????????????
// ????????????????????????????????????????????????????????????????

// ~~ Initialize the app
const app = express();

// ~~ Set template engine
app.set('view engine', 'ejs');

// ~~ Import templates
app.set('views', path.join(__dirname, 'src/views'));

app.use(express.static(path.join(__dirname, '/public')));

// ~~ Pretty Logging to console
app.use(morgan)

// ~~ Parse body
app.use(express.urlencoded({ extended: true }));

// ~~ Parse cookies
app.use(cookieSession({
  name: 'session',
  keys: ['Secret Cookie Key']
}));

// ~~ Set request time to request obj
app.use((req, res, next) => {

  // ~~ Get Date
  let requrestTime = new Date();

  // ~~ Set Date and app name to req obj
  req.requestTime = requrestTime.toISOString;

  // ~~ Set App name for logging
  req.appName = 'TINY APP';

  // ~~ Start template varialbes
  req.templateVars = {};

  // ~~ Check for user
  if (req.session.user !== undefined) {
    // ~~ Set template variables
    req.templateVars.user = req.session.user;
    req.templateVars.tile = req.session.title;
  } else {
    
    // ~~ Set template varialbes to null
    req.templateVars.user = null;
  }

  // ~~ Log Data to console
  console.log(chalk.magenta.bold.inverse(`Request Time: ${requrestTime}`));

  next();
})



// ~~ Set Method override
app.use(methodOverride('_method'));

// ??????????????????????????? Routes ?????????????????????????????
app.use('/urls', viewRouter);
app.use('/user', userRouter);


module.exports = app;